package foxgame.model;

/**
 * Represents the pieces that can be placed on the board.
 */
public enum Piece {
    /**
     * Represents an empty space on the board.
     */
    EMPTY,
    /**
     * Represents a dog piece on the board.
     */
    DOG,
    /**
     * Represents a fox piece on the board.
     */
    FOX
}
